func init() {
	decodeHexFont(fontHex)
}

func decodeHexFont(hex string) {
	if len(hex) < 95*32 {
		return
	}
	for i := 0; i < 95; i++ {
		for j := 0; j < 16; j++ {
			pos := (i*16 + j) * 2
			if pos+2 > len(hex) {
				return
			}
			hi := hexNibble(hex[pos])
			lo := hexNibble(hex[pos+1])
			bitmapFont[i][j] = byte((hi << 4) | lo)
		}
	}
}

func hexNibble(c byte) byte {
	switch {
	case c >= '0' && c <= '9':
		return c - '0'
	case c >= 'A' && c <= 'F':
		return c - 'A' + 10
	case c >= 'a' && c <= 'f':
		return c - 'a' + 10
	default:
		return 0
	}
}

func (r *Renderer) DrawText(text string, x, y int) {
	clipX := r.clip.X
	clipW := r.clip.X + r.clip.W
	clipY := r.clip.Y
	clipH := r.clip.Y + r.clip.H

	for _, ch := range text {
		idx := int(ch) - 32
		if idx < 0 || idx >= 95 {
			idx = 0
		}
		glyph := &bitmapFont[idx]
		for row := 0; row < 16; row++ {
			bits := glyph[row]
			if bits == 0 {
				continue
			}
			absY := y + row
			if absY < clipY || absY >= clipH {
				continue
			}
			stride := r.fb.Width
			pixelIdx := absY*stride + x
			for col := 0; col < 8; col++ {
				if bits&(0x80>>uint(col)) != 0 {
					absX := x + col
					if absX < clipX || absX >= clipW {
						continue
					}
					r.fb.Pixels[pixelIdx] = r.textColor
				}
				pixelIdx++
			}
		}
		x += 8
		if x >= clipW {
			break
		}
	}
}
